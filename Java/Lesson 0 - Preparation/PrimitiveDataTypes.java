
public class PrimitiveDataTypes {

    public static void main(String[] args) {
        byte theByte = -125;
        short theShort = 32767;
        int theInt = 2147483647;
        long theLong = 9223372036854775807L;
        float theFloat = 3.14F;
        double theDouble = 3.141592653589793;
        char theChar = 'A';
        boolean theBoolean = true;
        System.out.println(theByte);
        System.out.println(theShort);
        System.out.println(theInt);
        System.out.println(theLong);
        System.out.println(theFloat);
        System.out.println(theDouble);
        System.out.println(theChar);
        System.out.println(theBoolean);
    }

}
